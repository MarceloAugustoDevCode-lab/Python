

lista = []
for c in range(0, 5):
    n = int(input('Digite um valor: '))
    lista.append(n)

    if n > 0 < 1 in lista:
        lista.insert(0,n)
    if n > 1 < 2 in lista:
        lista.insert(1,n)
    if n > 2 < 3 in lista:
        lista.insert(2,n)
    if n > 3 < 4 in lista:
        lista.insert(3,n)
    if n > 4 in lista:
        lista.insert(4,n)

print(lista)